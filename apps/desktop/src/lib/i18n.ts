const messages = { es, en }

export type I18nKey = keyof typeof es | ExtraI18nKey
export type I18nParams = Record<string, string | number>

export const locale = writable<Locale>(DEFAULT_LOCALE)

function normalizeLocale(value: string | null | undefined): Locale {
  return value === 'en' ? 'en' : DEFAULT_LOCALE
}

function interpolate(template: string, params?: I18nParams): string {
  if (!params) return template
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? `{${key}}`))
}

function syncDocumentLanguage(nextLocale: Locale): void {
  if (typeof document !== 'undefined') {
    document.documentElement.lang = nextLocale
  }
}

locale.subscribe((value) => {
  syncDocumentLanguage(value)
})

export function t(key: I18nKey | (string & {}), params?: I18nParams): string {
  const dictionary = messages[get(locale)] as Record<string, string>
  return interpolate(dictionary[key] ?? String(key), params)
}

export const translator = derived(
  locale,
  () => (key: I18nKey, params?: I18nParams) => t(key, params)
)

export function isLocale(value: string | null | undefined): value is Locale {
  return value === 'es' || value === 'en'
}

export async function initLocale(): Promise<Locale> {
  const stored = await settingsGet(SETTINGS_KEYS.LANGUAGE).catch(() => null)
  const nextLocale = normalizeLocale(stored)
  locale.set(nextLocale)
  return nextLocale
}

export async function setLocale(nextLocale: Locale): Promise<void> {
  locale.set(nextLocale)
  await settingsSet(SETTINGS_KEYS.LANGUAGE, nextLocale)
}
